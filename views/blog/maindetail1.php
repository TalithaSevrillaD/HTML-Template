        <h1>Shoes Is A Statement Of All</h1>
        <img src="images/artikel1.jpg" class="img-fluid"><br>
        <div class="dropdown-divider"></div> 
        <p> In today's fashion world shoes have to be more versatile than ever. 
            Unless you stay home all the time, it's likely that you have to wear 
            shoes of some sort every day. The designers of the fashion world have responded
            with shoes for every occasion; even for those that don't like to wear shoes.
            In the world of business shoes that won't blister your feet are very important. 
            Many women that walk any distance bring tennis shoes for the walk and change into
            dress shoes once they arrive at their place of employment. When they do make that 
            change, it's not often that you'll see them change into any type of high heel. </p>
        <p> If attending an event such as a wedding or other banquet type party, the 
            high heeled shoes make a grand return. Most women will wear them to complement their dresses.
            They will either match or contrast with the dress that they have chosen to wear.
            Tennis shoes are a favorite when doing things like gardening, shopping, cleaning the house,
            etcetera. These shoes provide comfort and protection to the feet. If a woman is working in
            the yard or playing a game the tennis shoes will protect her feet while keeping them 
            comfortable. If she is spending the day shopping tennis shoes will make her day easier on 
            her at the end when she's walked for many hours. Sandals are the shoes of summer. Nicely 
            pedicured feet are just waiting to show off in those shoes that are nothing much more 
            than padding and straps. Toes are painted a variety of colors which adorn the clothing 
            and style choices.</p>
